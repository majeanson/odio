#!/usr/bin/env node
/**
 * ts-snippets.js — TypeScript snippet reference for Odio
 *
 * Reads features JSON array from stdin.
 * Extracts all codeSnippets[] entries, groups by domain.
 * Also includes publicInterface[] entries as type declarations.
 *
 * Output: Markdown reference with fenced code blocks.
 */

let raw = ''
process.stdin.on('data', chunk => { raw += chunk })
process.stdin.on('end', () => {
  const features = JSON.parse(raw)

  const DOMAIN_ORDER = [
    'app-shell', 'auth', 'recording', 'editing', 'sessions',
    'versioning', 'collaboration', 'band', 'render', 'storage'
  ]

  // Gather snippets + public interface entries by domain
  const byDomain = {}

  for (const f of features) {
    const domain = f.domain || 'misc'
    if (!byDomain[domain]) byDomain[domain] = { snippets: [], interfaces: [] }

    // Code snippets
    if (f.codeSnippets && f.codeSnippets.length > 0) {
      for (const s of f.codeSnippets) {
        byDomain[domain].snippets.push({
          featureKey: f.featureKey,
          featureTitle: f.title,
          componentFile: f.componentFile,
          label: s.label,
          snippet: s.snippet,
        })
      }
    }

    // Public interface entries
    if (f.publicInterface && f.publicInterface.length > 0) {
      for (const e of f.publicInterface) {
        byDomain[domain].interfaces.push({
          featureKey: f.featureKey,
          featureTitle: f.title,
          componentFile: f.componentFile,
          name: e.name,
          type: e.type,
          description: e.description,
        })
      }
    }
  }

  const totalSnippets = Object.values(byDomain).reduce((n, d) => n + d.snippets.length, 0)
  const totalInterfaces = Object.values(byDomain).reduce((n, d) => n + d.interfaces.length, 0)

  const lines = []
  lines.push(`# Odio — TypeScript Snippets & API Reference`)
  lines.push(``)
  lines.push(`> Generated by \`lac gen --generator ts-snippets\`  `)
  lines.push(`> ${totalSnippets} code snippets · ${totalInterfaces} interface entries · ${features.length} features`)
  lines.push(``)

  if (totalSnippets === 0 && totalInterfaces === 0) {
    lines.push(`> **No code snippets or publicInterface entries found.**`)
    lines.push(`>`)
    lines.push(`> Run \`lac fill --fields codeSnippets,publicInterface\` in feature directories to populate these fields.`)
    lines.push(``)
    process.stdout.write(lines.join('\n'))
    return
  }

  const orderedDomains = [
    ...DOMAIN_ORDER.filter(d => byDomain[d]),
    ...Object.keys(byDomain).filter(d => !DOMAIN_ORDER.includes(d)),
  ]

  for (const domain of orderedDomains) {
    const { snippets, interfaces } = byDomain[domain]
    if (snippets.length === 0 && interfaces.length === 0) continue

    const label = domain.replace(/-/g, ' ').replace(/\b\w/g, c => c.toUpperCase())
    lines.push(`## ${label}`)
    lines.push(``)

    if (interfaces.length > 0) {
      lines.push(`### Public Interface`)
      lines.push(``)
      for (const e of interfaces) {
        lines.push(`#### \`${e.name}\` — ${e.featureTitle}`)
        lines.push(``)
        if (e.description) lines.push(`> ${e.description}`)
        lines.push(``)
        lines.push('```typescript')
        lines.push(`// ${e.type}`)
        if (e.componentFile) lines.push(`// from: ${e.componentFile}`)
        lines.push('```')
        lines.push(``)
      }
    }

    if (snippets.length > 0) {
      lines.push(`### Code Snippets`)
      lines.push(``)
      for (const s of snippets) {
        lines.push(`#### ${s.label}`)
        lines.push(``)
        lines.push(`_From \`${s.featureKey}\` — ${s.featureTitle}${s.componentFile ? ` · \`${s.componentFile}\`` : ''}_`)
        lines.push(``)
        // Detect language from content heuristics
        const lang = s.snippet.includes('import ') || s.snippet.includes('const ') || s.snippet.includes('function ')
          ? 'typescript'
          : s.snippet.includes('SELECT') || s.snippet.includes('CREATE TABLE')
            ? 'sql'
            : s.snippet.includes('{') && s.snippet.includes(':')
              ? 'json'
              : 'typescript'
        lines.push('```' + lang)
        lines.push(s.snippet)
        lines.push('```')
        lines.push(``)
      }
    }
  }

  process.stdout.write(lines.join('\n'))
})
